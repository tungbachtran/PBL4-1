package Class;

public class Files {
	private String ClassFile;
	private String Object;
	public Files() {
		super();
	}
	public Files(String class1, String object) {
		super();
		ClassFile = class1;
		Object = object;
	}
	public String getClassFile() {
		return ClassFile;
	}
	public void setClassFile(String classFile) {
		ClassFile = classFile;
	}
	public String getObject() {
		return Object;
	}
	public void setObject(String object) {
		Object = object;
	}
	
	
	
	
	
}
